/* ======================================================
   HELPER: NATURAL TTS via server-side API
   Providers: Gemini TTS (Kore) → Cloud TTS (Neural2-F) → z-ai TTS (kazi)
   No browser speechSynthesis fallback — server must provide audio
   ====================================================== */
// Cache audio blobs by listening item ID so we don't re-fetch
const audioBlobCache = new Map<string, string>();

async function generateNaturalSpeech(itemId: string, text: string, accessToken?: string): Promise<string> {
  // Check cache first
  const cached = audioBlobCache.get(itemId);
  if (cached) return cached;

  if (!text || !text.trim()) {
    throw new Error('No text provided for audio generation');
  }

  // Build headers — include Authorization Bearer token as a belt-and-suspenders approach.
  // The HttpOnly cookie should be sent automatically via credentials: 'same-origin',
  // but some browsers/environments may not send it. The Bearer header serves as a reliable fallback.
  const headers: Record<string, string> = { 'Content-Type': 'application/json' };
  if (accessToken && accessToken !== 'null' && accessToken !== 'undefined') {
    headers['Authorization'] = `Bearer ${accessToken}`;
  }

  const res = await fetch('/api/tts/', {
    method: 'POST',
    headers,
    body: JSON.stringify({ text: text.trim() }),
    credentials: 'same-origin', // Explicitly include HttpOnly cookies
  });

  if (!res.ok) {
    const errData = await res.json().catch(() => ({}));
    const provider = res.headers.get('X-TTS-Provider') || 'unknown';
    console.error(`[TTS] Generation failed (status ${res.status}, provider: ${provider}):`, errData);
    throw new Error(`TTS generation failed (${res.status}): ${errData.error || errData.message || res.statusText}`);
  }

  // Log which provider was used
  const provider = res.headers.get('X-TTS-Provider') || 'unknown';
  console.debug(`[TTS] Audio generated by: ${provider}`);

  // Verify the response is actually audio data
  const contentType = res.headers.get('Content-Type') || '';
  if (contentType.includes('application/json')) {
    // Server returned JSON instead of audio — something went wrong
    const jsonErr = await res.json().catch(() => ({}));
    throw new Error(`TTS returned unexpected JSON: ${jsonErr.error || 'Unknown error'}`);
  }

  const blob = await res.blob();
  if (blob.size < 100) {
    throw new Error('TTS returned empty or too-small audio data');
  }
  const url = URL.createObjectURL(blob);
  audioBlobCache.set(itemId, url);
  return url;
}

